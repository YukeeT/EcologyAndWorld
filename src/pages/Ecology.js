const Ecology = () => {
  const location = useLocation();
  const [activeTopic, setActiveTopic] = useState('importance');

  useEffect(() => {
    // Проверяем хеш в URL при загрузке страницы
    if (location.hash) {
      const topicId = location.hash.substring(1); // Убираем #
      const topicExists = topics.find(t => t.id === topicId);
      if (topicExists) {
        setActiveTopic(topicId);
      }
    }
  }, [location.hash]);

  const activeTopicData = topics.find(topic => topic.id === activeTopic);

  return (
    <div className="ecology-page">
      <div className="ecology-hero">
        <div className="container">
          <div className="ecology-hero-content">
            <Link to="/" className="back-button">← Назад</Link>
            <h1>Экология и окружающая среда</h1>
            <p className="ecology-hero-subtitle">Изучайте важные темы экологии и узнавайте, как защитить нашу планету</p>
          </div>
        </div>
      </div>

      <div className="ecology-content-wrapper">
        <div className="container">
          <div className="ecology-layout">
            <aside className="ecology-sidebar">
              <h3>Темы</h3>
              <ul className="topics-list">
                {topics.map((topic) => (
                  <li key={topic.id}>
                    <button
                      className={`topic-button ${activeTopic === topic.id ? 'active' : ''}`}
                      onClick={() => setActiveTopic(topic.id)}
                    >
                      <span className="topic-icon">{topic.icon}</span>
                      <span className="topic-title">{topic.title}</span>
                    </button>
                  </li>
                ))}
              </ul>
            </aside>

            <main className="ecology-main-content">
              {activeTopicData && (
                <article className="topic-article">
                  <div className="topic-header">
                    <span className="topic-header-icon">{activeTopicData.icon}</span>
                    <h2>{activeTopicData.content.title}</h2>
                  </div>
                  <div className="topic-body">
                    {activeTopicData.content.paragraphs.map((paragraph, index) => (
                      <p key={index} className="topic-paragraph">
                        {paragraph}
                      </p>
                    ))}
                  </div>
                </article>
              )}
            </main>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Ecology;

